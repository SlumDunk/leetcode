package com.github.leetcode.vo;

/**
 * node of the tree
 * 
 * @author liuzhongda
 *
 */
public class TreeNode {
	/**
	 * value of the node
	 */
	public int val;
	/**
	 * left child of the node
	 */
	public TreeNode left;
	/**
	 * right child of the node
	 */
	public TreeNode right;

	public TreeNode(int x) {
		val = x;
	}

}
